mylist = ["carrot","cat","bee","honey",34]
nums = [1,2,34,324,45,6,7]
print(mylist)
print(nums)
print("\n")

#Accessing elements of list
print(mylist[2])
print(nums[3])
print("\n")

#sorting
print(nums.sort())
print(nums)
print("\n")

#reversing
print(nums.reverse())
print(nums)
print("\n")

# max and min
print(max(nums))
print(min(nums))


